// Assignment Operators
x = 6;
y = 8;

// += ?
x += y; // x = x + y, x = 6 + 8 , x =14
console.log(x);

x += 2; // x = x + 2, x = 14 + 2, x = 16
console.log(x);

// -= ?
x -= y; // x = x - y, x = 16 - 8, x = 8
console.log(x);

x -= 2; // x = x - 2, x = 8 - 2, x = 6
console.log(x);

// *= ?
x *= y; // x = x * y, x = 6 * 8, x = 48

x *= 2; // x = x * 2, x = 48 * 2, x = 96

// /= ?
x /= y; // x = x / y, x = 96 / 8, x = 12
console.log(x);

x /= 2; // x = x / 2, x = 12 / 2, x = 6
console.log(x);

// %= ?
x %= y; // x = x % y, x = 6 % 8, x = 6
console.log(x);

x %= 2; // x = x % 2, x = 6 % 2, x = 0
console.log(x);

// **= ?
y **= 2; // y = y pow 2
console.log(y);

// ++ ?
x++; // x = x + 1, x = 0 + 1
console.log(x);

x--; // x = x - 1, x = 1 - 1, x = 0
console.log(x);

